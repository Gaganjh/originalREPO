package com.manulife.pension.service.withdrawal.valueobject;

public class MultiPayeeMoneyType {
	
	private String moneyTypeId;
	private String taxCode;
	private String rothInd;
	public String getMoneyTypeId() {
		return moneyTypeId;
	}
	public void setMoneyTypeId(String moneyTypeId) {
		this.moneyTypeId = moneyTypeId;
	}
	public String getTaxCode() {
		return taxCode;
	}
	public void setTaxCode(String taxCode) {
		this.taxCode = taxCode;
	}
	public String getRothInd() {
		return rothInd;
	}
	public void setRothInd(String rothInd) {
		this.rothInd = rothInd;
	}
	
	
}
